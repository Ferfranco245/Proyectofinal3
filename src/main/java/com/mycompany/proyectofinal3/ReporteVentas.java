    private void inicializarTabla() {
    String[] columnas = {"Nombre Cliente", "NIT", "Direcci√≥n", "Total", "Total sin IVA", "Vendedor", "Fecha"};
    DefaultTableModel modelo = new DefaultTableModel(columnas, 0); // 0 indica que no hay filas al inicio
    jTable1.setModel(modelo); // Asocia el modelo a la tabla
}


   
   private void actualizarTabla() {
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0); // Limpiar filas existentes

    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
    double ivaPorcentaje = 15.0; // Porcentaje de IVA

    for (RegistroVentas venta : listaRegistroVentas) {
        double totalConIva = venta.getTotal();
        double totalSinIva = totalConIva / (1 + (ivaPorcentaje / 100));

        modelo.addRow(new Object[]{
            venta.getNombreCliente(),
            venta.getNit(),
            venta.getDireccion(),
            String.format("Q%.2f", totalConIva),
            String.format("Q%.2f", totalSinIva),
            venta.getVendedor(),
            sdf.format(venta.getFecha())
        });
    }
}

