    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String usuario = jTextField1.getText();
        String password = jTextField2.getText();
        
        boolean existe = false;
        
        for(Usuario u : Proyectofinal3.usuarios){
            if(u.getUsuario().equals(usuario) && u.getPassword().equals(password)){
                existe = true;
                if(u.getRol() == 1){
                   Ventanaadministrador v = new Ventanaadministrador();
                   v.setVisible(true);
                }
                else{
                    Ventanavendedor v = new Ventanavendedor(u);
                    v.setVisible(true);
                InformeVentas ventanaVentas = new InformeVentas(u.getUsuario());
                ventanaVentas.setLocationRelativeTo(null); // Centrar la ventana
                
                }
            }
        }
        
        if(existe == false){
            JOptionPane.showMessageDialog(this, "Usuario o password incorrecto");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

