    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    usuarioactual.setNombre(jTextField1.getText());
    usuarioactual.setUsuario(jTextField2.getText());
    usuarioactual.setPassword(jTextField3.getText());

    JOptionPane.showMessageDialog(this, "Datos actualizados exitosamente");
    
    for (Usuario u : Proyectofinal3.usuarios) {
            if (u.getUsuario().equals(usuarioactual.getUsuario())) {
                u.setNombre(usuarioactual.getNombre());
                u.setPassword(usuarioactual.getPassword());
                break;
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

