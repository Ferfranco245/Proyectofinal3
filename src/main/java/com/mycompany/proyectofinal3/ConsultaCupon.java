    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     int modificar = jTable1.getSelectedRow();  // Obtener la fila seleccionada

if (modificar > -1) {
    Cupon cupon = Proyectofinal3.cupones.get(modificar);

    // Actualizar el código del cupón
    cupon.setCodigo(jTextField1.getText());  // Código del cupón

    // Obtener y actualizar el descuento
    double valor = Double.parseDouble(jTextField2.getText());  // Obtener el descuento
    cupon.setDescuento(valor);  // Establecer el descuento

    // Obtener y actualizar el tipo del cupón (porcentaje o monto)
    String tipo = (String) jComboBox1.getSelectedItem();  // Obtener el tipo desde el combo box
    if ("Porcentaje".equalsIgnoreCase(tipo)) {
        cupon.setTipo("porcentaje");  // Establecer tipo como "porcentaje"
    } else {
        cupon.setTipo("monto");  // Establecer tipo como "monto"
    }

    // Validar y convertir la fecha de vencimiento
    try {
        String fechaTexto = jTextField3.getText();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate fechaVencimiento = LocalDate.parse(fechaTexto, formatter);  // Convertir texto a LocalDate
        cupon.setFechaVencimiento(fechaVencimiento);  // Establecer la fecha en el cupón
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Fecha inválida. Usa el formato YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
        return;  // Salir si la fecha es inválida
    }

    // Actualizar la tabla con los nuevos datos
    pintarTabla();

    // Mostrar mensaje de éxito
    JOptionPane.showMessageDialog(this, "Cambio guardado con éxito.");
} else {
    // Mostrar mensaje si no se seleccionó ninguna fila
    JOptionPane.showMessageDialog(this, "Seleccione un cupón para guardar los cambios.");
}                                           
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      int filaSeleccionada = jTable1.getSelectedRow();
    
    if (filaSeleccionada > -1) {
        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este cupón?");
        
        if (confirmacion == JOptionPane.YES_OPTION) {
            Proyectofinal3.cupones.remove(filaSeleccionada);
            pintarTabla(); // Actualiza la tabla después de borrar
            JOptionPane.showMessageDialog(this, "Cupón eliminado");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un cupón para borrar");

    }
    }//GEN-LAST:event_jButton2ActionPerformed

