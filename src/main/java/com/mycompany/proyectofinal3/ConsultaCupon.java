    public void pintarTabla() {
     DefaultTableModel modeloTabla = (DefaultTableModel) jTable1.getModel();
    modeloTabla.setRowCount(0); // Limpiar tabla

    for (Cupon cupon : Proyectofinal3.cupones) {
        String descuentoTexto;

        if ("porcentaje".equalsIgnoreCase(cupon.getTipo())) {
            descuentoTexto = String.format("%.0f", cupon.getDescuento()) + "%";
        } else {
            descuentoTexto = String.format("%.2f", cupon.getDescuento());
        }

        modeloTabla.addRow(new Object[]{
            cupon.getCodigo(),
            descuentoTexto,
            cupon.getTipo().equalsIgnoreCase("porcentaje") ? "Porcentaje" : "Monto", // ✅ Mostrar tipo
            cupon.getFechaVencimiento()
        });
    }
}
  public void actualizarTabla() {
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0); // Limpiar la tabla

    for (Cupon cupon : Proyectofinal3.cupones) {
        String tipoMostrar = cupon.getTipo().equalsIgnoreCase("porcentaje") ? "Porcentaje" : "Monto";
        String descuentoTexto = cupon.getTipo().equalsIgnoreCase("porcentaje")
                ? String.format("%.0f%%", cupon.getDescuento())
                : String.format("%.2f", cupon.getDescuento());

        Object[] fila = {
            cupon.getCodigo(),
            tipoMostrar,
            descuentoTexto,
            cupon.getFechaVencimiento().toString()
        };
        modelo.addRow(fila);
    }
}

    

    

