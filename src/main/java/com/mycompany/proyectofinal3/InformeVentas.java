    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
        String libro = jComboBox1.getSelectedItem().toString();
        int cantidad = Integer.parseInt(jTextField1.getText());
        double precio = Double.parseDouble(jTextField2.getText());
        double total = precio * cantidad;

        // Agregar a la tabla
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{libro, cantidad, precio, total});

        // Limpiar solo los campos relacionados al libro
        jComboBox1.setSelectedIndex(-1);
        jTextField1.setText("");
        jTextField2.setText("");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Por favor llena los campos correctamente.");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

