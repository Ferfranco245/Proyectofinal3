    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                                             
    double totalVenta = 0.0;

    // Recorremos todas las filas de la tabla
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    for (int i = 0; i < model.getRowCount(); i++) {
        double totalFila = (double) model.getValueAt(i, 3); // columna 3 = total por libro
        totalVenta += totalFila;
    }

    // Mostrar el total en el campo correspondiente
    jTextField6.setText(String.valueOf(totalVenta));

    // Obtener datos del cliente y vendedor
    String nombre = jTextField3.getText();
    String nit = jTextField4.getText();
    String direccion = jTextField5.getText();
    String vendedor = jTextField7.getText();
    Date fechaActual = new Date(); // Fecha actual

    // Crear y registrar el objeto RegistroVentas
    RegistroVentas nuevaVenta = new RegistroVentas(nombre, nit, direccion, totalVenta, vendedor, fechaActual);
    Proyectofinal3.listaRegistroVentas.add(nuevaVenta);

    // Mostrar mensaje de confirmación
    JOptionPane.showMessageDialog(this,
        "Venta registrada exitosamente.\n" +
        "Cliente: " + nombre + "\n" +
        "NIT: " + nit + "\n" +
        "Dirección: " + direccion + "\n" +
        "Vendedor: " + vendedor + "\n" +
        "Fecha: " + new SimpleDateFormat("dd/MM/yyyy").format(fechaActual) + "\n" +
        "Total: Q" + String.format("%.2f", totalVenta)); 
    
    // Limpiar campos de texto
    jTextField3.setText("");
    jTextField4.setText("");
    jTextField5.setText("");
    jTextField6.setText("");

    // Limpiar la tabla
    model.setRowCount(0);
    }//GEN-LAST:event_jButton1ActionPerformed

