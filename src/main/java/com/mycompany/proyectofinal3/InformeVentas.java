       private void actualizarPrecio() {
        String libroSeleccionado = (String) jComboBox1.getSelectedItem();
    if (libroSeleccionado != null) {
        switch (libroSeleccionado) {
            case "La Llorona":
                jTextField2.setText("120"); // Precio para La Llorona
                break;
            case "Romeo y Julieta":
                jTextField2.setText("150"); // Precio para Romeo y Julieta
                break;
            case "El principito":
                jTextField2.setText("100"); // Precio para El Principito
                break;
            case "Quijote de la mancha":
                jTextField2.setText("200"); // Precio para Quijote de la Mancha
                break;
            default:
                jTextField2.setText(""); // Si no hay selección
                break;
        }
}
       }


